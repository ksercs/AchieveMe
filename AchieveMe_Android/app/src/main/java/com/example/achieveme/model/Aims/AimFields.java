package com.example.achieveme.model.Aims;

public class AimFields {

    private String name;
    private String deadline;
    private boolean is_imortant;
    private boolean is_remind;
    private boolean is_completed;
    private int time_to_do;
    private String image;

    public String getName() {
        return name;
    }

    public String getDeadline() {
        return deadline;
    }

    public boolean isIs_imortant() {
        return is_imortant;
    }

    public boolean isIs_remind() {
        return is_remind;
    }

    public boolean isIs_completed() {
        return is_completed;
    }

    public int getTime_to_do() {
        return time_to_do;
    }

    public String getImage() {
        return image;
    }
}
